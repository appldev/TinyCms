CREATE VIEW dbo.AllPageFolders
AS
WITH Folders AS (SELECT        Id, ParentId, PageHostId, Name, 0 AS folderlevel, FolderIndex, CAST(Name AS varchar(400)) AS folderpath
                                       FROM            dbo.PageFolder
                                       WHERE        (ParentId IS NULL)
                                       UNION ALL
                                       SELECT        f.Id, f.ParentId, f.PageHostId, f.Name, parent.folderlevel + 1 AS folderlevel, f.FolderIndex, CAST(parent.folderpath + f.Name + '/' AS varchar(400)) AS folderpath
                                       FROM            dbo.PageFolder AS f INNER JOIN
                                                                Folders AS parent ON parent.Id = f.ParentId)
    SELECT        TOP (100) PERCENT Id, ParentId, PageHostId, Name, folderlevel, FolderIndex, folderpath
     FROM            Folders AS AllFolders
GO
